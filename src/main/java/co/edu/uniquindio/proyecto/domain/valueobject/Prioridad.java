package co.edu.uniquindio.proyecto.domain.valueobject;

/**
 * Representa el nivel de prioridad de una solicitud dentro del sistema.
 *
 * <p>Se utiliza para definir la urgencia o relevancia de la atención de la solicitud.</p>
 *
 * <ul>
 *     <li>ALTA: Solicitud de máxima prioridad.</li>
 *     <li>MEDIA: Solicitud de prioridad intermedia.</li>
 *     <li>BAJA: Solicitud de menor prioridad.</li>
 * </ul>
 */
public enum Prioridad {
    ALTA,
    MEDIA,
    BAJA
}